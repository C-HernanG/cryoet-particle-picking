import os
import argparse
import shutil

# Add paths BEFORE any project imports
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.dirname(SCRIPT_DIR)))
PROPICKER_DIR = os.path.join(PROJECT_ROOT, "tools", "ProPicker")
PROPICKER_INNER_DIR = os.path.join(PROPICKER_DIR, "propicker")

# Add ProPicker tools to path (for utils.mrctools)
# DeepETPicker_ProPicker is inside propicker/, so we need to chdir there
sys.path.insert(0, PROPICKER_INNER_DIR)
os.chdir(PROPICKER_INNER_DIR)

# Add project root to path for paths.py
sys.path.insert(0, PROJECT_ROOT)

# Add experiments to path for config import
sys.path.insert(0, os.path.join(PROJECT_ROOT, "experiments"))

# Now import project modules
from utils.mrctools import load_mrc_data, save_mrc_data
from paths import (
    PROPICKER_MODEL_FILE,
    UMU_SYNTH_TOMOS_DIR,
    UMU_SYNTH_LABELS_DIR,
    EXP3_COORDS_DIR,
    EXP3_FINETUNING_DIR,
    EXP3_RESULTS_DIR,
    EXP3_CHECKPOINTS_DIR,
)
from experiments.config import (
    EXP3_TRAIN_POOL,
    EXP3_VAL_TOMOS,
    EXP3_INCREMENTS,
    EXP3_MAX_EPOCHS,
    EXP3_BATCH_SIZE,
    EXP3_BLOCK_SIZE,
    EXP3_PAD_SIZE,
    EXP3_LEARNING_RATE,
    EXP3_LABEL_DIAMETER,
    EXP3_GPU_ID,
    EXP3_USE_BINARY_LABELS,
    EXP3_SAVE_EVERY_N_EPOCHS,
    EXP3_SAVE_BEST,
)
import pandas as pd
import gc
import glob

# Disable warnings for chained assignments in pandas
pd.options.mode.chained_assignment = None

# =============================================================================
# CONFIGURATION
# =============================================================================

# Prompt embedding file (generated by notebook)
prompt_embed_file = os.path.join(
    str(EXP3_RESULTS_DIR), "fixed_prompts_umusynth_thy.json")

# =============================================================================
# HELPER FUNCTIONS
# =============================================================================


def get_tomo_id_from_name(tomo_name):
    """Extract numeric ID from tomogram name (e.g., 'tomo_rec_0_snr1.63' -> 0)"""
    parts = tomo_name.split("_")
    return int(parts[2])


def get_label_filename(tomo_name):
    """Get corresponding label filename for a tomogram"""
    tomo_id = get_tomo_id_from_name(tomo_name)
    return f"tomo_lbls_{tomo_id}.mrc"


def get_coords_filename(tomo_name):
    """Get corresponding coords filename for a tomogram"""
    return f"{tomo_name}_thyroglobulin_coords.csv"


def find_best_checkpoint(runs_dir):
    """Find the best checkpoint from training runs"""
    version_dirs = sorted(glob.glob(os.path.join(runs_dir, "*", "version_*")))
    if not version_dirs:
        return None
    
    latest_version = version_dirs[-1]
    ckpt_dir = os.path.join(latest_version, "checkpoints")
    ckpts = glob.glob(os.path.join(ckpt_dir, "*.ckpt"))
    
    if not ckpts:
        return None
    
    # Return the checkpoint with lowest validation loss (if available) or last epoch
    ckpts.sort(key=lambda x: int(x.split("epoch=")[1].split("-")[0]) if "epoch=" in x else 0)
    return ckpts[-1]


def train_increment(n_train_tomos, out_dir, skip_if_exists=True):
    """
    Train ProPicker with a specific number of training tomograms.
    
    Args:
        n_train_tomos: Number of training tomograms to use
        out_dir: Output directory for this increment
        skip_if_exists: Skip training if checkpoint already exists
    
    Returns:
        Path to best checkpoint, or None if training failed
    """
    print("\n" + "=" * 70)
    print(f"TRAINING INCREMENT: {n_train_tomos} tomograms")
    print("=" * 70)
    
    # Check if already trained
    checkpoint_dir = os.path.join(str(EXP3_CHECKPOINTS_DIR), f"increment_{n_train_tomos}")
    best_ckpt_path = os.path.join(checkpoint_dir, "best_model.ckpt")
    
    if skip_if_exists and os.path.exists(best_ckpt_path):
        print(f"✅ Checkpoint already exists: {best_ckpt_path}")
        print("   Skipping training (use --force to retrain)")
        return best_ckpt_path
    
    # Select training tomograms
    train_tomos = EXP3_TRAIN_POOL[:n_train_tomos]
    val_tomos = EXP3_VAL_TOMOS
    all_tomos = train_tomos + val_tomos
    
    print(f"\nTraining set ({len(train_tomos)}): {train_tomos}")
    print(f"Validation set ({len(val_tomos)}): {val_tomos}")
    
    # Setup output directory
    if os.path.exists(out_dir):
        print(f"Removing existing output directory: {out_dir}")
        shutil.rmtree(out_dir)
    
    os.makedirs(out_dir)
    os.makedirs(f"{out_dir}/raw_data")
    
    # =========================================================================
    # Prepare data in DeepETPicker format
    # =========================================================================
    print("\nPreparing data in DeepETPicker format...")
    
    for i, tomo_name in enumerate(all_tomos):
        print(f"  [{i+1}/{len(all_tomos)}] Processing {tomo_name}...")
        
        # Load tomogram
        tomo_file = os.path.join(str(UMU_SYNTH_TOMOS_DIR), f"{tomo_name}.mrc")
        tomo = load_mrc_data(tomo_file).float()
        
        # Invert contrast (TomoTwin convention)
        tomo = -1 * tomo
        
        # Save tomogram for preprocessing
        save_mrc_data(tomo, f"{out_dir}/raw_data/{tomo_name}.mrc")
        del tomo
        
        # Load coordinates from preprocessed file
        coords_file = os.path.join(
            str(EXP3_COORDS_DIR), get_coords_filename(tomo_name))
        if not os.path.exists(coords_file):
            print(f"  WARNING: Coords file not found: {coords_file}")
            continue
        
        coords_df = pd.read_csv(coords_file)
        
        # Save coords in DeepETPicker format
        coords_df[['X', 'Y', 'Z']].to_csv(
            f"{out_dir}/raw_data/{tomo_name}.coords",
            index=False, sep="\t", header=False
        )
        
        del coords_df
        gc.collect()
    
    # =========================================================================
    # Generate DeepETPicker preprocessing config
    # =========================================================================
    print("\nGenerating preprocessing configuration...")
    
    os.makedirs(f"{out_dir}/configs", exist_ok=True)
    pre_config_file = f"{out_dir}/configs/preprocess.py"
    
    lines = [
        "pre_config={",
        f'"dset_name": "umusynth_inc{n_train_tomos}",',
        f'"base_path": "{out_dir}",',
        f'"coord_path": "{out_dir}/raw_data",',
        f'"coord_format": ".coords",',
        f'"tomo_path": "{out_dir}/raw_data",',
        f'"tomo_format": ".mrc",',
        f'"num_cls": 1,',
        f'"label_type": "gaussian",',
        f'"label_diameter": {EXP3_LABEL_DIAMETER},',
        f'"ocp_type": "sphere",',
        f'"ocp_diameter": "{EXP3_LABEL_DIAMETER}",',
        f'"norm_type": "standardization"',
        "}"
    ]
    
    with open(pre_config_file, "w") as f:
        for line in lines:
            f.write(line + "\n")
    
    # Run preprocessing
    print("\nRunning DeepETPicker preprocessing...")
    os.system(
        f"python ./DeepETPicker_ProPicker/bin/preprocess.py --pre_configs {pre_config_file}")
    
    # =========================================================================
    # Generate training config
    # =========================================================================
    print("\nGenerating training configuration...")
    
    train_set_ids = f"0-{len(train_tomos)-1}"
    val_set_ids = f"{len(train_tomos)}-{len(all_tomos)-1}"
    
    os.system(
        f"python ./DeepETPicker_ProPicker/bin/generate_train_config.py "
        f"--pre_configs '{pre_config_file}' "
        f"--dset_name 'train' "
        f"--cfg_save_path '{out_dir}/configs' "
        f"--train_set_ids {train_set_ids} "
        f"--val_set_ids {val_set_ids} "
        f"--batch_size {EXP3_BATCH_SIZE} "
        f"--block_size {EXP3_BLOCK_SIZE} "
        f"--pad_size {EXP3_PAD_SIZE} "
        f"--learning_rate {EXP3_LEARNING_RATE} "
        f"--max_epoch {EXP3_MAX_EPOCHS} "
        f"--gpu_id {EXP3_GPU_ID}"
    )
    
    # =========================================================================
    # Replace gaussian labels with binary labels (if configured)
    # =========================================================================
    if EXP3_USE_BINARY_LABELS:
        print("\nReplacing gaussian labels with binary labels...")
        
        label_dirs = glob.glob(f"{out_dir}/gaussian*")
        if len(label_dirs) != 1:
            raise AssertionError(
                f"Expected exactly one label directory, found {len(label_dirs)}")
        label_dir = label_dirs[0]
        
        for tomo_name in all_tomos:
            label_file = os.path.join(
                str(UMU_SYNTH_LABELS_DIR), get_label_filename(tomo_name))
            label = load_mrc_data(label_file).float()
            save_mrc_data(label, f"{label_dir}/{tomo_name}.mrc")
            del label
            gc.collect()
    
    # =========================================================================
    # Run training
    # =========================================================================
    print("\n" + "=" * 70)
    print(f"Starting training with {n_train_tomos} tomograms...")
    print("=" * 70)
    
    os.system(
        f"python ./DeepETPicker_ProPicker/bin/train_bash.py "
        f"--train_configs '{out_dir}/configs/train.py' "
        f"--network ProPicker "
        f"--propicker_model_file '{PROPICKER_MODEL_FILE}' "
        f"--prompt_embed_file '{prompt_embed_file}' "
        f"--loss_func_seg 'CE'"
    )
    
    # =========================================================================
    # Copy best checkpoint to organized directory
    # =========================================================================
    print("\nSaving best checkpoint...")
    
    runs_dir = os.path.join(out_dir, "runs", "train")
    best_ckpt = find_best_checkpoint(runs_dir)
    
    if best_ckpt:
        os.makedirs(checkpoint_dir, exist_ok=True)
        shutil.copy2(best_ckpt, best_ckpt_path)
        print(f"✅ Best checkpoint saved to: {best_ckpt_path}")
        
        # Also save training config for reference
        train_config_src = f"{out_dir}/configs/train.py"
        train_config_dst = os.path.join(checkpoint_dir, "train_config.py")
        if os.path.exists(train_config_src):
            shutil.copy2(train_config_src, train_config_dst)
    else:
        print("❌ No checkpoint found after training!")
        return None
    
    # Cleanup raw data to save space
    print("\nCleaning up raw data...")
    shutil.rmtree(f"{out_dir}/raw_data")
    
    return best_ckpt_path


# =============================================================================
# MAIN SCRIPT
# =============================================================================

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Incremental fine-tuning of ProPicker on UMU Synthetic dataset")
    parser.add_argument(
        "--increment", type=int, default=None,
        help="Run only this specific increment (number of training tomos)")
    parser.add_argument(
        "--force", action="store_true",
        help="Force retraining even if checkpoint exists")
    args = parser.parse_args()
    
    print("=" * 70)
    print("ProPicker Incremental Fine-Tuning on UMU Synthetic Dataset")
    print("=" * 70)
    
    # Check prerequisites
    if not os.path.exists(prompt_embed_file):
        print(f"\n❌ ERROR: Prompt embeddings not found: {prompt_embed_file}")
        print("Please run the preprocessing notebook first!")
        sys.exit(1)
    
    if not os.path.exists(str(EXP3_COORDS_DIR)):
        print(f"\n❌ ERROR: Coordinates directory not found: {EXP3_COORDS_DIR}")
        print("Please run the preprocessing notebook first!")
        sys.exit(1)
    
    print(f"\n✅ Prerequisites found")
    print(f"  Prompt embeddings: {prompt_embed_file}")
    print(f"  Coordinates dir: {EXP3_COORDS_DIR}")
    
    # Determine which increments to run
    if args.increment is not None:
        if args.increment not in EXP3_INCREMENTS:
            print(f"\n❌ ERROR: Invalid increment {args.increment}")
            print(f"   Valid increments: {EXP3_INCREMENTS}")
            sys.exit(1)
        increments_to_run = [args.increment]
    else:
        increments_to_run = EXP3_INCREMENTS
    
    print(f"\n📋 Increments to train: {increments_to_run}")
    print(f"   Epochs per increment: {EXP3_MAX_EPOCHS}")
    print(f"   Validation set: {EXP3_VAL_TOMOS}")
    
    # Train each increment
    results = {}
    for n_tomos in increments_to_run:
        out_dir = os.path.join(str(EXP3_FINETUNING_DIR), f"increment_{n_tomos}")
        ckpt_path = train_increment(
            n_train_tomos=n_tomos,
            out_dir=out_dir,
            skip_if_exists=not args.force
        )
        results[n_tomos] = ckpt_path
    
    # Summary
    print("\n" + "=" * 70)
    print("TRAINING SUMMARY")
    print("=" * 70)
    
    for n_tomos, ckpt_path in results.items():
        status = "✅" if ckpt_path else "❌"
        print(f"  {status} Increment {n_tomos:2d}: {ckpt_path or 'FAILED'}")
    
    print(f"\n📁 Checkpoints saved to: {EXP3_CHECKPOINTS_DIR}")
    print("\nNext step: Run inference with umusynth_inference.py")
    print("=" * 70)

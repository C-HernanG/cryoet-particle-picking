#!/usr/bin/env python3
"""
Fine-tuning script for ProPicker on UMU Synthetic Thyroglobulin dataset.
Based on empiar10988_fine_tuning.py

PREREQUISITE: Run the preprocessing notebook first to generate:
- Coordinates in results/exp2_umusynth_thy/coords/
- Prompt embeddings in results/exp2_umusynth_thy/fixed_prompts_umusynth_thy.json

Usage:
    cd /home/carlos-hg/Dev/cryoet-particle-picking/tools/ProPicker
    conda activate deepetpicker
    python ../../experiments/exp2_umusynth_thy/scripts/umusynth_fine_tuning.py
"""

import sys
import os

# Add ProPicker to path BEFORE any other imports
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.dirname(SCRIPT_DIR)))
PROPICKER_DIR = os.path.join(PROJECT_ROOT, "tools", "ProPicker")
sys.path.insert(0, PROPICKER_DIR)
os.chdir(PROPICKER_DIR)

import shutil
import glob
import gc
import pandas as pd

from utils.mrctools import load_mrc_data, save_mrc_data

# Import project paths
sys.path.insert(0, PROJECT_ROOT)
from paths import (
    PROPICKER_MODEL_FILE, 
    UMU_SYNTH_TOMOS_DIR,
    UMU_SYNTH_LABELS_DIR,
    EXP2_COORDS_DIR,
)

# Disable warnings for chained assignments in pandas
pd.options.mode.chained_assignment = None

# =============================================================================
# CONFIGURATION
# =============================================================================

# Training/Validation split (20 train, 5 val)
train_tomos = [
    "tomo_rec_0_snr1.63", "tomo_rec_1_snr1.46", "tomo_rec_2_snr1.07", "tomo_rec_3_snr0.63",
    "tomo_rec_4_snr1.85", "tomo_rec_10_snr0.97", "tomo_rec_11_snr1.41", "tomo_rec_12_snr1.39",
    "tomo_rec_13_snr1.1", "tomo_rec_14_snr0.43", "tomo_rec_15_snr1.03", "tomo_rec_16_snr0.93",
    "tomo_rec_17_snr0.92", "tomo_rec_18_snr0.78", "tomo_rec_19_snr1.62", "tomo_rec_20_snr0.73",
    "tomo_rec_21_snr0.34", "tomo_rec_22_snr0.97", "tomo_rec_23_snr0.29", "tomo_rec_24_snr1.39"
]
val_tomos = [
    "tomo_rec_5_snr1.66", "tomo_rec_6_snr1.17", "tomo_rec_7_snr1.13",
    "tomo_rec_8_snr0.57", "tomo_rec_9_snr1.28"
]

# Training parameters (same as empiar10988)
max_epochs = 50
batch_size = 1
block_size = 72
pad_size = 12
learning_rate = 1e-4
label_diameter = 21
gpu_id = 0

# Use binary labels from dataset (same as empiar10988)
use_binary_labels = True

# Output directory
out_dir = os.path.join(PROJECT_ROOT, "results", "exp2_umusynth_thy", "fine_tuning_deepetpicker")

# Prompt embedding file (generated by notebook)
prompt_embed_file = os.path.join(PROJECT_ROOT, "results", "exp2_umusynth_thy", "fixed_prompts_umusynth_thy.json")

# =============================================================================
# HELPER FUNCTIONS
# =============================================================================

def get_tomo_id_from_name(tomo_name):
    """Extract numeric ID from tomogram name (e.g., 'tomo_rec_0_snr1.63' -> 0)"""
    parts = tomo_name.split("_")
    return int(parts[2])


def get_label_filename(tomo_name):
    """Get corresponding label filename for a tomogram"""
    tomo_id = get_tomo_id_from_name(tomo_name)
    return f"tomo_lbls_{tomo_id}.mrc"


def get_coords_filename(tomo_name):
    """Get corresponding coords filename for a tomogram"""
    return f"{tomo_name}_thyroglobulin_coords.csv"


# =============================================================================
# MAIN SCRIPT
# =============================================================================

if __name__ == "__main__":
    print("=" * 70)
    print("ProPicker Fine-Tuning on UMU Synthetic Thyroglobulin Dataset")
    print("=" * 70)
    
    # Check prerequisites
    if not os.path.exists(prompt_embed_file):
        print(f"\n❌ ERROR: Prompt embeddings not found: {prompt_embed_file}")
        print("Please run the preprocessing notebook first!")
        sys.exit(1)
    
    if not os.path.exists(str(EXP2_COORDS_DIR)):
        print(f"\n❌ ERROR: Coordinates directory not found: {EXP2_COORDS_DIR}")
        print("Please run the preprocessing notebook first!")
        sys.exit(1)
    
    print(f"\n✅ Prerequisites found")
    print(f"  Prompt embeddings: {prompt_embed_file}")
    print(f"  Coordinates dir: {EXP2_COORDS_DIR}")
    
    # =========================================================================
    # STEP 1: Setup output directory
    # =========================================================================
    print("\n" + "=" * 70)
    print("STEP 1: Setup directories")
    print("=" * 70)
    
    if os.path.exists(out_dir):
        response = input(f"Output directory {out_dir} already exists. Delete it? (y/n): ")
        if response.lower() == 'y':
            shutil.rmtree(out_dir)
        else:
            print("Exiting...")
            sys.exit(1)
    
    os.makedirs(out_dir)
    os.makedirs(f"{out_dir}/raw_data")
    
    # =========================================================================
    # STEP 2: Prepare data in DeepETPicker format
    # =========================================================================
    print("\n" + "=" * 70)
    print("STEP 2: Prepare data in DeepETPicker format")
    print("=" * 70)
    
    all_tomos = train_tomos + val_tomos
    
    print(f"\nProcessing {len(all_tomos)} tomograms...")
    print(f"  Training: {len(train_tomos)}")
    print(f"  Validation: {len(val_tomos)}")
    
    for i, tomo_name in enumerate(all_tomos):
        print(f"\n[{i+1}/{len(all_tomos)}] Processing {tomo_name}...")
        
        # Load tomogram
        tomo_file = os.path.join(str(UMU_SYNTH_TOMOS_DIR), f"{tomo_name}.mrc")
        tomo = load_mrc_data(tomo_file).float()
        
        # Invert contrast (TomoTwin convention - same as empiar10988)
        tomo = -1 * tomo
        
        # Save tomogram for preprocessing
        save_mrc_data(tomo, f"{out_dir}/raw_data/{tomo_name}.mrc")
        del tomo
        
        # Load coordinates from preprocessed file
        coords_file = os.path.join(str(EXP2_COORDS_DIR), get_coords_filename(tomo_name))
        if not os.path.exists(coords_file):
            print(f"  WARNING: Coords file not found: {coords_file}")
            continue
            
        coords_df = pd.read_csv(coords_file)
        
        # Save coords in DeepETPicker format (x, y, z without header, tab-separated)
        # IMPORTANT: Save to raw_data/ directory (same as empiar10988_fine_tuning.py)
        # DeepETPicker preprocessing will copy these to coords/ and generate num_name.csv
        coords_df[['X', 'Y', 'Z']].to_csv(
            f"{out_dir}/raw_data/{tomo_name}.coords",
            index=False, sep="\t", header=False
        )
        
        del coords_df
        gc.collect()
    
    # =========================================================================
    # STEP 3: Generate DeepETPicker preprocessing config
    # =========================================================================
    print("\n" + "=" * 70)
    print("STEP 3: Generate preprocessing configuration")
    print("=" * 70)
    
    os.makedirs(f"{out_dir}/configs", exist_ok=True)
    pre_config_file = f"{out_dir}/configs/preprocess.py"
    
    # IMPORTANT: coord_path and tomo_path point to raw_data/
    # DeepETPicker preprocessing will:
    # 1. Copy coords to {base_path}/coords/ and generate num_name.csv
    # 2. Normalize tomograms to {base_path}/data_std/
    # 3. Generate gaussian labels to {base_path}/gaussian{diameter}/
    # This is the same flow as empiar10988_fine_tuning.py
    lines = [
        "pre_config={",
        f'"dset_name": "umusynth_preprocess",',
        f'"base_path": "{out_dir}",',
        f'"coord_path": "{out_dir}/raw_data",',
        f'"coord_format": ".coords",',
        f'"tomo_path": "{out_dir}/raw_data",',
        f'"tomo_format": ".mrc",',
        f'"num_cls": 1,',
        f'"label_type": "gaussian",',
        f'"label_diameter": {label_diameter},',
        f'"ocp_type": "sphere",',
        f'"ocp_diameter": "{label_diameter}",',
        f'"norm_type": "standardization"',
        "}"
    ]
    
    with open(pre_config_file, "w") as f:
        for line in lines:
            f.write(line + "\n")
    
    # Run preprocessing
    print("\nRunning DeepETPicker preprocessing...")
    os.system(f"python ./DeepETPicker_ProPicker/bin/preprocess.py --pre_configs {pre_config_file}")
    
    # =========================================================================
    # STEP 4: Generate training config
    # =========================================================================
    print("\n" + "=" * 70)
    print("STEP 4: Generate training configuration")
    print("=" * 70)
    
    train_set_ids = f"0-{len(train_tomos)-1}"
    val_set_ids = f"{len(train_tomos)}-{len(all_tomos)-1}"
    
    os.system(
        f"python ./DeepETPicker_ProPicker/bin/generate_train_config.py "
        f"--pre_configs '{pre_config_file}' "
        f"--dset_name 'train' "
        f"--cfg_save_path '{out_dir}/configs' "
        f"--train_set_ids {train_set_ids} "
        f"--val_set_ids {val_set_ids} "
        f"--batch_size {batch_size} "
        f"--block_size {block_size} "
        f"--pad_size {pad_size} "
        f"--learning_rate {learning_rate} "
        f"--max_epoch {max_epochs} "
        f"--gpu_id {gpu_id}"
    )
    
    # =========================================================================
    # STEP 5: Replace gaussian labels with binary labels
    # =========================================================================
    if use_binary_labels:
        print("\n" + "=" * 70)
        print("STEP 5: Replace gaussian labels with binary labels from dataset")
        print("=" * 70)
        
        label_dirs = glob.glob(f"{out_dir}/gaussian*")
        if len(label_dirs) != 1:
            raise AssertionError(f"Expected exactly one label directory, found {len(label_dirs)}")
        label_dir = label_dirs[0]
        
        for tomo_name in all_tomos:
            # Load binary label from dataset
            label_file = os.path.join(str(UMU_SYNTH_LABELS_DIR), get_label_filename(tomo_name))
            label = load_mrc_data(label_file).float()
            
            # Save to DeepETPicker label directory
            save_mrc_data(label, f"{label_dir}/{tomo_name}.mrc")
            
            print(f"  {tomo_name}: label shape {label.shape}")
            
            del label
            gc.collect()
    
    # =========================================================================
    # STEP 6: Run training
    # =========================================================================
    print("\n" + "=" * 70)
    print("STEP 6: Starting training...")
    print("=" * 70)
    
    os.system(
        f"python ./DeepETPicker_ProPicker/bin/train_bash.py "
        f"--train_configs '{out_dir}/configs/train.py' "
        f"--network ProPicker "
        f"--propicker_model_file '{PROPICKER_MODEL_FILE}' "
        f"--prompt_embed_file '{prompt_embed_file}' "
        f"--loss_func_seg 'CE'"
    )
    
    # =========================================================================
    # Cleanup
    # =========================================================================
    print("\nCleaning up raw data...")
    shutil.rmtree(f"{out_dir}/raw_data")
    
    print("\n" + "=" * 70)
    print("Fine-tuning complete!")
    print(f"Output directory: {out_dir}")
    print("=" * 70)

#!/usr/bin/env python3
"""
Incremental Fine-tuning script for ProPicker on UMU Synthetic Thyroglobulin dataset.
Based on exp2's umusynth_fine_tuning.py, adapted for incremental training analysis.

This script trains ProPicker multiple times with increasing amounts of training data
to analyze data efficiency and identify the minimum required annotations.

PREREQUISITE: Run the preprocessing notebook first to generate:
- Coordinates in results/exp3_ppicker_limits/coords/
- Prompt embeddings in results/exp3_ppicker_limits/fixed_prompts_umusynth_thy.json

Usage:
    cd /path/to/cryoet-particle-picking/tools/ProPicker
    conda activate deepetpicker
    
    # Run all increments:
    python ../../experiments/exp3_ppicker_limits/scripts/umusynth_fine_tuning.py
    
    # Run a specific increment:
    python ../../experiments/exp3_ppicker_limits/scripts/umusynth_fine_tuning.py --increment 4
"""


#!/usr/bin/env python3
"""
Fine-tuning script for ProPicker on EMPIAR-10988 Ribosome dataset.
Adapted from ProPicker tutorial2 to work from experiments directory.

Usage:
    cd /path/to/cryoet-particle-picking/tools/ProPicker
    conda activate deepetpicker
    python ../../experiments/exp1_empiar10988_ribo/scripts/empiar10988_fine_tuning.py
"""

from utils.mrctools import load_mrc_data, save_mrc_data
from data.preparation_functions.prepare_empiar10988 import (
    empiar10988_ts_to_slice_of_interest,
    get_empiar10988_coord_dict
)
from paths import PROPICKER_MODEL_FILE, EMPIAR10988_BASE_DIR, EXP1_RESULTS_DIR
from experiments.config import (
    EXP1_TRAIN_TS,
    EXP1_VAL_TS,
    EXP1_CROP_DELTA,
    EXP1_CROP_DELTA_EPOCHS,
    EXP1_BATCH_SIZE,
    EXP1_BLOCK_SIZE,
    EXP1_PAD_SIZE,
    EXP1_LEARNING_RATE,
    EXP1_LABEL_DIAMETER,
    EXP1_GPU_ID,
    EXP1_USE_BINARY_LABELS,
)
import pandas as pd
import glob
import shutil
import sys
import os

# Add paths BEFORE any project imports
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.dirname(SCRIPT_DIR)))
PROPICKER_DIR = os.path.join(PROJECT_ROOT, "tools", "ProPicker")

# Add ProPicker tools to path (for utils, data, etc.)
sys.path.insert(0, PROPICKER_DIR)
os.chdir(PROPICKER_DIR)

# Add project root to path for paths.py
sys.path.insert(0, PROJECT_ROOT)

# Add experiments to path for config import
sys.path.insert(0, os.path.join(PROJECT_ROOT, "experiments"))

# Now import project modules

# Disable warnings for chained assignments in pandas
pd.options.mode.chained_assignment = None

# =============================================================================
# CONFIGURATION (from config.py)
# =============================================================================

train_ts = EXP1_TRAIN_TS
val_ts = EXP1_VAL_TS
crop_delta = EXP1_CROP_DELTA
crop_delta_num_epochs_dict = EXP1_CROP_DELTA_EPOCHS
use_binary_labels = EXP1_USE_BINARY_LABELS
gpu_id = EXP1_GPU_ID

# Output directory
out_dir = os.path.join(str(EXP1_RESULTS_DIR),
                       "fine_tuning_deepetpicker", f"crop_delta={crop_delta}")

# Prompt embedding file (generated by notebook)
prompt_embed_file = os.path.join(
    str(EXP1_RESULTS_DIR), "fixed_prompts_empiar10988.json")

# =============================================================================
# MAIN SCRIPT
# =============================================================================

if __name__ == "__main__":
    print("=" * 70)
    print("ProPicker Fine-Tuning on EMPIAR-10988 Ribosome Dataset")
    print("=" * 70)

    # Check prerequisites
    if not os.path.exists(prompt_embed_file):
        print(f"\n❌ ERROR: Prompt embeddings not found: {prompt_embed_file}")
        print("Please run the preprocessing notebook first!")
        sys.exit(1)

    print(f"\n✅ Prerequisites found")
    print(f"  Prompt embeddings: {prompt_embed_file}")
    print(f"  EMPIAR10988 data: {EMPIAR10988_BASE_DIR}")

    # Create output directory
    if os.path.exists(out_dir):
        response = input(
            f"Output directory {out_dir} already exists. Delete it? (y/n): ")
        if response.lower() == 'y':
            shutil.rmtree(out_dir)
        else:
            print("Exiting...")
            sys.exit(1)
    os.makedirs(out_dir)

    # Load tomograms, coordinates (and binary labels) and crop them
    print(f"\nProcessing {len(train_ts) + len(val_ts)} tomograms...")
    print(f"  Training: {train_ts}")
    print(f"  Validation: {val_ts}")
    print(f"  Crop delta: {crop_delta}")

    ts_id_gt_locmap_dict = {}
    for id, ts_id in enumerate(train_ts + val_ts):
        print(f"\n[{id+1}/{len(train_ts)+len(val_ts)}] Processing {ts_id}...")

        # Load tomogram
        tomo_file = f"{EMPIAR10988_BASE_DIR}/tomograms/{ts_id}.rec"
        slice_of_interest = empiar10988_ts_to_slice_of_interest[ts_id]
        # IMPORTANT: tomogram has to be scaled to contrast 'white particles on dark background'!
        tomo = -1 * load_mrc_data(tomo_file).float()[slice_of_interest]

        # Setup cropping function for tomogram (and binary labels)
        z_slice = empiar10988_ts_to_slice_of_interest[ts_id]
        z_width = z_slice.stop - z_slice.start
        z_center = z_width // 2
        z_limit = [z_center - crop_delta, z_center + crop_delta]
        y_limit = [464 - crop_delta, 464 + crop_delta]
        x_limit = [480 - crop_delta, 480 + crop_delta]
        x_limit = [max(0, x_limit[0]), min(x_limit[1], tomo.shape[2])]
        y_limit = [max(0, y_limit[0]), min(y_limit[1], tomo.shape[1])]
        z_limit = [max(0, z_limit[0]), min(z_limit[1], tomo.shape[0])]

        def crop_tomo_fn(tomo):
            return tomo[
                z_limit[0]:z_limit[1],
                y_limit[0]:y_limit[1],
                x_limit[0]:x_limit[1]
            ]

        # Crop tomogram
        tomo = crop_tomo_fn(tomo)

        # Save cropped tomogram
        if not os.path.exists(f"{out_dir}/raw_data"):
            os.makedirs(f"{out_dir}/raw_data")
        save_mrc_data(tomo, f"{out_dir}/raw_data/{ts_id}.mrc")

        # Load particle coordinates
        coords_dir = f"{EMPIAR10988_BASE_DIR}/particle_lists"
        coord_dict = get_empiar10988_coord_dict(coords_dir, ts_id)
        coord_dict = {"cyto_ribosome": coord_dict["cyto_ribosome"]}
        for k, coords in coord_dict.items():
            coords = coords[
                (coords.x >= x_limit[0]) & (coords.x < x_limit[1]) &
                (coords.y >= y_limit[0]) & (coords.y < y_limit[1]) &
                (coords.z >= z_limit[0]) & (coords.z < z_limit[1])
            ]
            coords.x -= x_limit[0]
            coords.y -= y_limit[0]
            coords.z -= z_limit[0]
            coord_dict[k] = coords

        coords = coords[["x", "y", "z"]]
        coords.to_csv(f"{out_dir}/raw_data/{ts_id}.coords",
                      index=False, sep="\t", header=None)

        # Load binary labels
        if use_binary_labels:
            locmap_file = f"{EMPIAR10988_BASE_DIR}/labels/{ts_id}_cyto_ribosomes.mrc"
            gt_locmap_dict = {"cyto_ribosome": load_mrc_data(
                locmap_file)[slice_of_interest].float()}
            gt_locmap_dict = {k: crop_tomo_fn(v)
                              for k, v in gt_locmap_dict.items()}
            ts_id_gt_locmap_dict[ts_id] = gt_locmap_dict["cyto_ribosome"]

    # Generate DeepETPicker preprocessing config
    print("\n" + "=" * 70)
    print("Generating preprocessing configuration...")
    print("=" * 70)

    os.makedirs(f"{out_dir}/configs", exist_ok=True)
    pre_config_file = f"{out_dir}/configs/preprocess.py"
    lines = [
        "pre_config={",
        f'"dset_name": "empiar10988_preprocess",',
        f'"base_path": "{out_dir}",',
        f'"coord_path": "{out_dir}/raw_data",',
        f'"coord_format": ".coords",',
        f'"tomo_path": "{out_dir}/raw_data",',
        f'"tomo_format": ".mrc",',
        f'"num_cls": 1,',
        f'"label_type": "gaussian",',
        f'"label_diameter": {EXP1_LABEL_DIAMETER},',
        f'"ocp_type": "sphere",',
        f'"ocp_diameter": "{EXP1_LABEL_DIAMETER}",',
        f'"norm_type": "standardization"',
        "}"
    ]
    with open(pre_config_file, "w") as f:
        for line in lines:
            f.write(line + "\n")

    # Run preprocessing
    print("\nRunning DeepETPicker preprocessing...")
    os.system(
        f"python ./DeepETPicker_ProPicker/bin/preprocess.py --pre_configs {pre_config_file}")

    # Generate DeepETPicker training config
    print("\n" + "=" * 70)
    print("Generating training configuration...")
    print("=" * 70)

    train_set_ids = '0' if len(train_ts) == 1 else f'0-{len(train_ts)-1}'
    val_set_ids = f'{len(train_ts)}' if len(
        val_ts) == 1 else f'{len(train_ts)}-{len(train_ts)+len(val_ts)-1}'

    os.system(
        f"python ./DeepETPicker_ProPicker/bin/generate_train_config.py "
        f"--pre_configs '{pre_config_file}' "
        f"--dset_name 'train' "
        f"--cfg_save_path '{out_dir}/configs' "
        f"--train_set_ids {train_set_ids} "
        f"--val_set_ids {val_set_ids} "
        f"--batch_size {EXP1_BATCH_SIZE} "
        f"--block_size {EXP1_BLOCK_SIZE} "
        f"--pad_size {EXP1_PAD_SIZE} "
        f"--learning_rate {EXP1_LEARNING_RATE} "
        f"--max_epoch {crop_delta_num_epochs_dict[crop_delta]} "
        f"--gpu_id {gpu_id}"
    )

    # If use_binary_labels, replace DeepETPicker's gaussian labels with binary labels
    if use_binary_labels:
        print("\n" + "=" * 70)
        print("Replacing gaussian labels with binary labels...")
        print("=" * 70)

        label_dir = glob.glob(f"{out_dir}/gaussian*")
        if len(label_dir) != 1:
            raise AssertionError(
                f"Expected exactly one label directory, found {len(label_dir)}")

        for ts_id, locmap in ts_id_gt_locmap_dict.items():
            save_mrc_data(locmap, f"{label_dir[0]}/{ts_id}.mrc")
            print(f"  {ts_id}: label shape {locmap.shape}")

    # Run training
    print("\n" + "=" * 70)
    print("Starting training...")
    print("=" * 70)

    os.system(
        f"python ./DeepETPicker_ProPicker/bin/train_bash.py "
        f"--train_configs '{out_dir}/configs/train.py' "
        f"--network ProPicker "
        f"--propicker_model_file '{PROPICKER_MODEL_FILE}' "
        f"--prompt_embed_file '{prompt_embed_file}' "
        f"--loss_func_seg 'CE'"
    )

    # Remove raw data
    print("\nCleaning up raw data...")
    shutil.rmtree(f"{out_dir}/raw_data")

    print("\n" + "=" * 70)
    print("Fine-tuning complete!")
    print(f"Output directory: {out_dir}")
    print("=" * 70)
